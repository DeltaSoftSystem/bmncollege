        <h2 class="green_title">Best Practice - II
            <span class="second">" Training and Facilitating Participation of Student Cultural Teams in Literary, Fine Arts, Theatre and Dance at State, National and International Level "</span></h2>
        
        <!--Introduction-->
        <div class="padd_bor">
        <div class="row">
            
            <div class="col-sm-12">
                <h3 class="research_title bca">Introduction</h3>
                <p class="practice_txt">Formation of Student cultural teams, training and facilitating them for participation in various 
categories of events like Literary, Fine Arts, Theatre and Dance at Inter-collegiate, State, National &amp; International Level.</p>
                
            </div>
        </div>
        </div>
        <!--Introduction-->
        
        <!--Context-->
        <div class="padd_bor">
        <div class="row">
            
            <div class="col-sm-12">
                <h3 class="research_title bca">Context  </h3>
                <span class="challeng_title">Challenges faced :</span>
                <ul class="audit_bx">
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        As Student Teams are from different Departments of the College, coordinating a common time for organizing Training and Practice Sessions.</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Students had to submit all their Assignments and appear for Class Tests which they had missed, all together in one go, after the events. However, as some students faced problems in completing them all with the deadlines some common guidelines for students and teachers were given.</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        As all were Girl Students, arranging sessions late after the lectures and calling them during the holidays also was problematic. </li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Limitation in selecting a role and for Dances, (as it is a Girl’s College), girls only had to play the boys’ role as well</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        The limitation was time, as the College has Professional Courses and students have full day College they get less time for their practice.</li>
                </ul>
                
            </div>
        </div>
        </div>
        <!--Context-->
        
        <!--Goal-->
        <div class="padd_bor">
        <div class="row">
           
            <div class="col-sm-12">
                <h3 class="research_title bca">Goal  </h3>
                <ul class="audit_bx">
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Main Objective: To showcase the rich talent of the students. </li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        To help students develop their talent, improve their skills in all aspects - Organizational, Presentation, Leadership and Interpersonal Communication</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        To inculcate Team Spirit and overall Personality Development in students</li>
                </ul>
                
            </div>
        </div>
        </div>
        <!--Goal-->
        
        <!--The Practice-->
        <div class="padd_bor">

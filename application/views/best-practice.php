            <div class="col-sm-12">
                <h3 class="research_title bca">The Practice  </h3>
                <ul class="audit_bx">
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Students are motivated and teams are formed. Directors, choreographers and experts for a particular team were appointed.</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Students are trained and participated in the respective events. All the expenses are budgeted and sponsored by the college.</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Students who missed classes and assessments are given an opportunity to catch up.</li>
                    <li><i class="fa fa-dot-circle-o" aria-hidden="true"></i> 
                        Monitoring of the same was done by cultural committee</li>
                </ul>
                
            </div>
